package physics;

public class Vector2d {
    private double x;
    private double y;

    public Vector2d(double _x, double _y){
        x = _x;
        y = _y;
    }

    public double get_x() {
        return x;
    }

    public double get_y() {
        return y;
    }

    @Override
    public String toString() {
        return "("+x+","+y+")";
    }
}
